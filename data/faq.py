def find_faq(query: str) -> Optional[Dict]:
    """Find FAQ matching the query using scoring system"""
    query_lower = query.lower()

    best_faq = None
    best_score = 0

    for faq in FAQ_DATA:
        score = 0
        for keyword in faq["keywords"]:
            if keyword in query_lower:
                # Longer keywords get higher score (more specific)
                score += len(keyword)

        if score > best_score:
            best_score = score
            best_faq = faq

    return best_faq


